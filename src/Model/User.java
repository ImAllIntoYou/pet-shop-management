package Model;

/**
 *
 * @author Admin
 */
abstract class User {
    protected String name;
    protected String status;

    public User(String name, String status) {
        this.name = name;
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
